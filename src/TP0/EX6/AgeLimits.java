package TP0.EX6;

public class AgeLimits {
    private int ageAlcool;
    private int ageFesseBouk;
    private int ageMaire;

    public AgeLimits(int ageAlcool, int ageFesseBouk, int ageMaire) {
        this.ageAlcool = ageAlcool;
        this.ageFesseBouk = ageFesseBouk;
        this.ageMaire = ageMaire;
    }

    public int getAgeAlcool() {
        return ageAlcool;
    }

    public int getAgeFesseBouk() {
        return ageFesseBouk;
    }

    public int getAgeMaire() {
        return ageMaire;
    }
}

